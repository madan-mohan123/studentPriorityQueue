package studentpriorityqueue;

public class Student {
private String name;
private int roll;
private double cpi;
//getter,setter
public void setName(String name) {
	this.name=name;
}
public void setCpi(double cpi) {
	this.cpi=cpi;
}
public void SetRoll(int roll) {
	this.roll=roll;
}
public String getName() {
	return name;
}
public double getCpi() {
	return cpi;
}
public int getRoll() {
	return roll;
}
}
